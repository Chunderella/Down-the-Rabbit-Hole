package com.rabbit.lang.ex04;

public class Exam19 {
  public static void main(String[] args) {
    
    float f;

    f = 9.876545f;  
    System.out.println(f);
    //9.876545
      
    f = 987654.5f;
    System.out.println(f);
    //987654.5
      
    f = 0.000009876545f;
    System.out.println(f);
    //9.876545E-6

    f = 98765456f;
    System.out.println(f);
    //9.8765456E7
    
    f = 9876545.6f;
    System.out.println(f);
    //9876546.0
  }
}




